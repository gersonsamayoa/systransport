    {
        Mail::send('admin.emails.contact', $request->all(), function($msj){
            $msj->subject('Correo de Contacto');
            $msj->to('gersonsamayoa@gmail.com');
        });

        flash('El correo fue enviado correctamente, pronto nos pondremos en contacto con usted')->warning()->important();
        return redirect()->route('admin.auth.contacto');
